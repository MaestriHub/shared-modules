package com.maestri.sdk.sources.shared.dto.enums

/// Перечисление `SalonType` определяет типы салонов, поддерживаемые в системе.
enum class SalonType {
    INDIVIDUAL, // Означает, что салон является индивидуальным предприятием.
    CHAIN,      // Означает, что салон является частью сети.
    MASTER      // Означает, что салон управляется мастером.
}
